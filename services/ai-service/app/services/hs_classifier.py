def classify(
    description: str,
    country_origin: Optional[str] = None,
    unit_price: Optional[float] = None
) -> list[dict]:
    """
    Classify goods description to HS code using keyword matching.
    Returns top-3 suggestions sorted by confidence.
    """
    if not description:
        return []
    
    description_lower = description.lower()
    matches = []
    
    # Check each HS code entry
    for hs_code, keywords in HS_KEYWORDS.items():
        match_count = 0
        exact_matches = 0
        
        for keyword in keywords:
            keyword_lower = keyword.lower()
            if keyword_lower in description_lower:
                match_count += 1
                # Check for exact word match (higher confidence)
                words = description_lower.split()
                if keyword_lower in words:
                    exact_matches += 1
        
        if match_count > 0:
            # Calculate confidence based on match quality
            base_confidence = min(0.9, 0.5 + (match_count * 0.1))
            exact_bonus = exact_matches * 0.1
            confidence = min(0.95, base_confidence + exact_bonus)
            
            # Дополнить код до 10 знаков нулями
            code_10 = _pad_hs_code(hs_code)
            name_ru = HS_NAMES.get(hs_code, HS_NAMES.get(code_10, f"Товар {code_10}"))
            
            matches.append({
                "hs_code": code_10,
                "name_ru": name_ru,
                "confidence": round(confidence, 2),
            })
    
    # Sort by confidence (descending) and return top-3
    matches.sort(key=lambda x: x["confidence"], reverse=True)
    
    return matches[:3]


def _pad_hs_code(code: str) -> str:
    """Нормализовать код ТН ВЭД до ровно 10 знаков.
    Min 6 цифр, группа 01-97.
    """
    import re
    c = re.sub(r"\D", "", code.strip())
    if len(c) < 6:
        return ""
    c = c.ljust(10, "0")[:10]
    try:
        g = int(c[:2])
        if g < 1 or g > 97:
            return ""
    except ValueError:
        return ""
    return c
