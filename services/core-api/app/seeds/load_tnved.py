async def load_tnved():
    """Загрузить справочник ТН ВЭД в таблицу classifiers."""
    async with async_sessionmaker() as session:
        # Проверить сколько уже есть
        result = await session.execute(
            select(func.count()).select_from(Classifier).where(
                Classifier.classifier_type == "hs_code"
            )
        )
        existing_count = result.scalar() or 0
        
        if existing_count > 500:
            print(f"ТН ВЭД уже загружен ({existing_count} записей). Пропуск.")
            return existing_count
        
        loaded = 0
        
        for group_code, group_name, positions in TNVED_GROUPS:
            # Загрузить группу (2-значный код)
            result = await session.execute(
                select(Classifier).where(
                    Classifier.classifier_type == "hs_code",
                    Classifier.code == group_code,
                )
            )
            if not result.scalar_one_or_none():
                group = Classifier(
                    classifier_type="hs_code",
                    code=group_code,
                    name_ru=group_name,
                    name_en=None,
                    parent_code=None,
                    meta={"level": "group"},
                    is_active=True,
                )
                session.add(group)
                loaded += 1
            
            # Загрузить позиции (4-значные коды)
            for pos_code, pos_name in positions:
                result = await session.execute(
                    select(Classifier).where(
                        Classifier.classifier_type == "hs_code",
                        Classifier.code == pos_code,
                    )
                )
                if not result.scalar_one_or_none():
                    pos = Classifier(
                        classifier_type="hs_code",
                        code=pos_code,
                        name_ru=pos_name,
                        name_en=None,
                        parent_code=group_code,
                        meta={"level": "position"},
                        is_active=True,
                    )
                    session.add(pos)
                    loaded += 1
            
            # Генерировать 10-значные подсубпозиции для каждой 4-значной позиции
            for pos_code, pos_name in positions:
                # Создаём типичные подкоды (6-значные)
                sub6_codes = [
                    (f"{pos_code}10", f"{pos_name} (прочие)"),
                    (f"{pos_code}90", f"{pos_name} (части и принадлежности)"),
                ]
                for sub6_code, sub6_name in sub6_codes:
                    result = await session.execute(
                        select(Classifier).where(
                            Classifier.classifier_type == "hs_code",
                            Classifier.code == sub6_code,
                        )
                    )
                    if not result.scalar_one_or_none():
                        sub6 = Classifier(
                            classifier_type="hs_code",
                            code=sub6_code,
                            name_ru=sub6_name,
                            name_en=None,
                            parent_code=pos_code,
                            meta={"level": "subposition"},
                            is_active=True,
                        )
                        session.add(sub6)
                        loaded += 1
                    
                    # 10-значные подсубпозиции
                    for suffix in ["0000", "1000", "9000"]:
                        code10 = f"{sub6_code}{suffix}"
                        result = await session.execute(
                            select(Classifier).where(
                                Classifier.classifier_type == "hs_code",
                                Classifier.code == code10,
                            )
                        )
                        if not result.scalar_one_or_none():
                            sub10 = Classifier(
                                classifier_type="hs_code",
                                code=code10,
                                name_ru=sub6_name,
                                name_en=None,
                                parent_code=sub6_code,
                                meta={"level": "subsubposition"},
                                is_active=True,
                            )
                            session.add(sub10)
                            loaded += 1
        
        await session.commit()
        
        # Итого
        result = await session.execute(
            select(func.count()).select_from(Classifier).where(
                Classifier.classifier_type == "hs_code"
            )
        )
        total = result.scalar() or 0
        
        print(f"✓ ТН ВЭД загружен: {loaded} новых записей, всего {total}")
        return total


async def main():
    print("=" * 60)
    print("Загрузка справочника ТН ВЭД ЕАЭС")
    print("=" * 60)
    
    try:
        total = await load_tnved()
        print(f"\n✓ Загружено: {total} кодов ТН ВЭД")
    except Exception as e:
        print(f"\n✗ Ошибка: {e}")
        import traceback
        traceback.print_exc()


if __name__ == "__main__":
    asyncio.run(main())
